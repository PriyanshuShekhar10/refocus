// Client-side authentication component for the navbar
function NavbarAuthButton() {
  const [user, setUser] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const router = useRouter();

  useEffect(() => {
    const supabase = createClient();

    // Get initial session
    const getSession = async () => {
      const {
        data: { session },
      } = await supabase.auth.getSession();
      setUser(session?.user || null);
      setLoading(false);
    };

    getSession();

    // Listen for auth changes
    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((event, session) => {
      setUser(session?.user || null);
      setLoading(false);
    });

    return () => subscription.unsubscribe();
  }, []);

  const logout = async () => {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/auth/login");
  };

  if (loading) {
    return (
      <div className="flex gap-2">
        <NavbarButton variant="secondary" disabled>
          Loading...
        </NavbarButton>
      </div>
    );
  }

  if (!hasEnvVars) {
    return (
      <div className="flex gap-4 items-center">
        <Badge variant={"outline"} className="font-normal">
          Supabase environment variables required
        </Badge>
        <div className="flex gap-2">
          <Button size="sm" variant={"outline"} disabled>
            Sign in
          </Button>
          <Button size="sm" variant={"default"} disabled>
            Sign up
          </Button>
        </div>
      </div>
    );
  }

  if (user) {
    return (
      <div className="flex items-center gap-4">
        {/* <span className="text-sm">Hey, {user.email}!</span> */}

        <NavbarButton variant="secondary" onClick={() => router.push("/profile")}>
          My Profile
        </NavbarButton>
        <NavbarButton variant="secondary" onClick={logout}>
          Logout
        </NavbarButton>
      </div>
    );
  }

  return (
    <div className="flex gap-2">
      <NavbarButton variant="secondary" href="/auth/login">
        Sign in
      </NavbarButton>
      <NavbarButton variant="primary" href="/auth/sign-up">
        Sign up
      </NavbarButton>
    </div>
  );
}

